                <p>iPhone 6 Plus mang trên mình một màn hình lớn đến 5,5 inch dẫn đến kích thước tổng thể lớn của một chiếc Phablet. Tuy vậy bạn vẫn có thể dễ dàng cầm máy gọn gàng đầy thoải mái nhờ việc bo tròn góc cạnh của máy. Phần khung nhôm nguyên khối thống nhất tạo nên sự liền lạc của mặt kính và bộ khung kim loại, kết cấu hợp lý, tất cả đều được gia công tỉ mỉ đến từng chi tiết nhỏ để nâng cao trải nghiệm của bạn. Thiết kế lớn hơn, đẹp hơn và trên hết là độ mỏng chỉ 7 mm - mỏng nhất so với những máy cùng kích thước màn hình,  mang đến cho bạn một sản phẩm vô cùng sang trọng, cao cấp.</p><br/>				<img class="img-rounded" src="Images/chitietsp2.jpg" width="100%" alt="Tin tức"/>
                <br/><br/><p>Điện thoại iPhone 6 Plus có một bước nâng cấp lớn về màn hình. Màn hình máy giờ không chỉ lớn hơn mà còn hiển thị tốt hơn bao giờ hết. Một màn hình iPhone hoàn toàn mới mẻ với màu sắc rực rỡ và độ tương phản cao hơn ở góc nhìn rộng hơn. Đó là những gì mà màn hình kích thước khổng lồ 5,5 inch và độ phân giải 1920x1080 pixel vượt chuẩn Retina mang lại. Những hình ảnh sắc nét, những thước phim sống động hay những game đồ họa kinh điển, tất cả đều nổi bật lên như thật ngay trước mắt bạn theo cách tuyệt vời nhất. </p><br/>
                		<img class="img-rounded" src="Images/chitietsp3.jpg" width="100%" alt="Tin tức"/>
                 <br/><br/><p>Vi xử lý A8 hoàn toàn mới, siêu mạnh mẽ, siêu tiết kiệm

 
iPhone 6 Plus được trang bị bộ vi xử lý A8 với nhiều điểm nâng cấp vượt trội so với vi xử lý A7 trên iPhone 5S. Vẫn trên nền tảng cấu trúc 64 bit, chip A8 cực kỳ mạnh, cho hiệu suất hơn A7 đến 25% và mạnh gấp 50 lần thế hệ iPhone đầu tiên. Đi dùng chip A8 là chip M8 bổ trợ thu thập mọi thông số về đo lường và những cảm biến mới. Khả năng xử lý đồ họa trên iPhone 6 đi lên một tầm cao mới khi bạn chơi những game 3D đồ họa phức tạp, những hiệu ứng bóng đổ, ánh sáng, hành động đều mượt mà. 
Những cải tiến của chip A8 không chỉ mạnh hơn mà còn tiết kiệm hơn. Kết hợp cùng viên pin dung lượng cao nhất từ trước đến nay thì iPhone 6 Plus sẽ đồng hành cùng bạn lâu hơn, sử dụng được nhiều hơn mà không cần phải sạc thường xuyên.</p>
